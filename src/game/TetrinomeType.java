package game;

/**
 * Enum representing the different types of Tetrinomes in Tetris.
 * Each Tetrinome type corresponds to a specific shape:
 *   I: A straight line
 *   O: A square
 *   T: A T-shaped block
 *   S: A zigzag block
 *   Z: A reverse zigzag block
 *   J: A J-shaped block
 *   L: An L-shaped block
 */
public enum TetrinomeType {
    /** A straight line Tetrinome. */
    I,

    /** A square Tetrinome. */
    O,

    /** A T-shaped Tetrinome. */
    T,

    /** A zigzag Tetrinome. */
    S,

    /** A reverse zigzag Tetrinome. */
    Z,

    /** A J-shaped Tetrinome. */
    J,

    /** An L-shaped Tetrinome. */
    L
}
